    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
       Luong_XayDung lg = new Luong_XayDung(txtPLuong.getText(),Float.parseFloat(txtLuongCB.getText().trim()),
                Float.parseFloat(txtPhuCap.getText().trim()),Float.parseFloat(txtThue.getText().trim()),txtmanv.getText());
     try {
         if (x.update(lg))
         {
             JOptionPane.showMessageDialog(this, "Sửa thành công", "Thông báo" , JOptionPane.INFORMATION_MESSAGE);
             tbLuong.setModel(x.getAll());
         }
         else
             JOptionPane.showMessageDialog(this, "Sủa không thành công", "Thông báo" , JOptionPane.INFORMATION_MESSAGE);
     } catch (SQLException ex) {
         Logger.getLogger(FormLuong.class.getName()).log(Level.SEVERE, null, ex);
     }  
    }//GEN-LAST:event_btnSuaActionPerformed

