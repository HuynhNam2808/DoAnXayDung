       txtPLuong.setText(tbLuong.getValueAt(row, 0).toString());
       txtLuongCB.setText(tbLuong.getValueAt(row, 1).toString());
       txtPhuCap.setText(tbLuong.getValueAt(row, 2).toString());
       txtThue.setText(tbLuong.getValueAt(row, 3).toString());
       txtmanv.setText(tbLuong.getValueAt(row, 4).toString());
